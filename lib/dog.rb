class Dog
    def initialize(name=nil, breed=nil)
        @name = name
        @breed = breed
    end

    def name=(new_name)
        @name = new_name
    end

    def name
        @name
    end

    def breed
        @breed
    end

    def breed=(input_breed)
        @breed = input_breed
    end
end
    


    
